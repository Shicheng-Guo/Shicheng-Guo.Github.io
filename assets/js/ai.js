// ai.js — renders the "AI in Pharma" tracker from assets/data/ai.json
// (built daily by the update-ai GitHub Action). Filter by category, search,
// sort by date. Reuses the .deal-table / .filter-pill styling.

(function () {
  const tableMount = document.getElementById("aiTable");
  if (!tableMount) return;
  const statusEl  = document.getElementById("aiStatus");
  const searchEl  = document.getElementById("aiSearch");
  const updatedEl = document.getElementById("aiUpdated");
  const countEl   = document.getElementById("aiCount");
  const catBox    = document.getElementById("aiCategory");
  const DATA_URL  = "/assets/data/ai.json";

  let all = [];
  let activeCat = "";
  let query = "";
  let sortDesc = true;

  function el(tag, attrs, ...kids) {
    const node = document.createElement(tag);
    if (attrs) for (const [k, v] of Object.entries(attrs)) {
      if (v == null || v === false) continue;
      if (k === "class") node.className = v;
      else if (k.startsWith("on") && typeof v === "function") node.addEventListener(k.slice(2), v);
      else node.setAttribute(k, v === true ? "" : String(v));
    }
    for (const c of kids.flat(Infinity)) {
      if (c == null || c === false || c === true) continue;
      node.appendChild(c instanceof Node ? c : document.createTextNode(String(c)));
    }
    return node;
  }

  function renderFilters() {
    if (!catBox) return;
    catBox.replaceChildren();
    const cats = ["", ...[...new Set(all.map(d => d.category).filter(Boolean))].sort()];
    cats.forEach(val => {
      const isActive = activeCat === val;
      catBox.appendChild(el("button", {
        class: "filter-pill" + (isActive ? " is-active" : ""),
        type: "button",
        "aria-pressed": isActive ? "true" : "false",
        onclick: () => { activeCat = val; renderFilters(); renderTable(); }
      }, val || "All"));
    });
  }

  function matches(d) {
    if (activeCat && d.category !== activeCat) return false;
    if (query && !d.headline.toLowerCase().includes(query)) return false;
    return true;
  }

  function catBadge(c) {
    const cls = "deal-badge deal-badge--" + (c || "news").toLowerCase().replace(/[^a-z]/g, "");
    return el("span", { class: cls }, c || "News");
  }

  function renderTable() {
    const rows = all.filter(matches);
    rows.sort((a, b) => sortDesc ? (b.date || "").localeCompare(a.date || "")
                                 : (a.date || "").localeCompare(b.date || ""));
    if (countEl) countEl.textContent = rows.length + (rows.length === 1 ? " item" : " items");

    tableMount.replaceChildren();
    if (!rows.length) {
      tableMount.appendChild(el("p", { class: "deal-empty" }, "No items match these filters."));
      return;
    }

    const head = el("tr", null,
      el("th", { class: "deal-th-date", onclick: () => { sortDesc = !sortDesc; renderTable(); }, title: "Sort by date" },
        "Date " + (sortDesc ? "▾" : "▴")),
      el("th", null, "News · Event · Perspective"),
      el("th", null, "Category"),
    );

    const body = rows.map(d => el("tr", null,
      el("td", { class: "deal-date" }, d.date || "—"),
      el("td", { class: "deal-headline" },
        el("a", { href: d.link, target: "_blank", rel: "noopener" }, d.headline),
        d.source && el("span", { class: "deal-source" }, d.source)
      ),
      el("td", null, catBadge(d.category)),
    ));

    tableMount.appendChild(
      el("div", { class: "deal-table-wrap" },
        el("table", { class: "deal-table" }, el("thead", null, head), el("tbody", null, body))
      )
    );
  }

  function setStatus(msg, isError) {
    if (!statusEl) return;
    statusEl.hidden = !msg;
    statusEl.textContent = msg || "";
    statusEl.classList.toggle("is-error", !!isError);
  }

  if (searchEl) searchEl.addEventListener("input", e => { query = e.target.value.trim().toLowerCase(); renderTable(); });

  setStatus("Loading AI updates…", false);
  fetch(DATA_URL, { cache: "no-cache" })
    .then(r => { if (!r.ok) throw new Error("HTTP " + r.status); return r.json(); })
    .then(data => {
      all = (data.items || []).filter(d => d.headline && d.link);
      setStatus("", false);
      if (updatedEl && data.generated) {
        const t = new Date(data.generated).toLocaleString(undefined, { dateStyle: "medium", timeStyle: "short" });
        updatedEl.textContent = "Last updated " + t + " · " + all.length + " items tracked";
      }
      renderFilters();
      renderTable();
    })
    .catch(() => setStatus("Couldn't load the AI data yet. It is generated by the daily update job — check back after the first run.", true));
})();
